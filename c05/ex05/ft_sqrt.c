int	ft_sqrt(int nb)
{
	long	x;
	int		y;

	x = nb;
	y = 1;
	if (nb < 0)
		return (0);
	while (x - y > 1)
	{
		x = (x + y) / 2;
		y = nb / x;
	}
	if (x * x == nb)
		return (x);
	return (0);
}
